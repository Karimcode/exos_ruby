puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

puts 3 + 2 < 5 - 7 # Comparaison entre deux nombres entiers

puts "Ça fait combien 3 + 2 ? #{3 + 2}" # Calcul de l'addition
puts "Ça fait combien 5 - 7 ? #{5 - 7}" # Calcul de l'addition

puts "Ok, c'est faux alors !"

puts "C'est drôle ça, faisons-en plus :"

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" # Comparaison => renvoie true
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"  # Comparaison => renvoie true
